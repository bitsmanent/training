/* Esercizio 2.29 (Cap. 2)
   Determina l'intero equivalente almeno di:
   A B C a b c 0 1 2 $ * + / e dello spazio. */

#include <stdio.h>

int main()
{
   printf("A = %d\tB = %d\tC = %d\n", 'A', 'B', 'C');
   printf("a = %d\tb = %d\tc = %d\n", 'a', 'b', 'c');
   printf("0 = %d\t1 = %d\t2 = %d\n", '0', '1', '2');
   printf("$ = %d\t* = %d\t+ = %d\n", '$', '*', '+');
   printf("/ = %d\t@ = %d\t^ = %d\n", '/', '@', '^');

   return 0;
} /* E0F main */

