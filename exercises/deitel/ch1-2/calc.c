/* Calcola il prodotto di tre interi */
#include <stdio.h>

/* Funzione principale */
int main()
{
   int x, y, z, result;

   printf("Imetti tre interi: ");
   scanf("%d%d%d", &x, &y, &z);

   result = x * y * z;

   printf("The product is");
   printf(" %d\n",result);

   return 0;

} /* E0F main */

